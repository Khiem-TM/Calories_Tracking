export enum FoodType {
  INGREDIENT = 'ingredient',
  DISH = 'dish',
  PRODUCT = 'product',
}

/*
    - INGREDIENT: Thành phần nguyên liệu, ví dụ: gạo, thịt, rau củ, ...
    - DISH: Món ăn đã được chế biến, ví dụ: cơm tấm, phở bò, ...
    - PRODUCT: Sản phẩm đóng gói, ví dụ: mì ăn liền, bánh mì, ...
*/
